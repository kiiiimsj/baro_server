public class CourseTitleBean {
    String courseIdTitle;
    String titleTitle;
    String deptNameTitle;
    String creditsTitle;

    public String getCourseIdTitle() {
        return courseIdTitle;
    }

    public void setCourseIdTitle(String courseIdTitle) {
        this.courseIdTitle = courseIdTitle;
    }

    public String getTitleTitle() {
        return titleTitle;
    }

    public void setTitleTitle(String titleTitle) {
        this.titleTitle = titleTitle;
    }

    public String getDeptNameTitle() {
        return deptNameTitle;
    }

    public void setDeptNameTitle(String deptNameTitle) {
        this.deptNameTitle = deptNameTitle;
    }

    public String getCreditsTitle() {
        return creditsTitle;
    }

    public void setCreditsTitle(String creditsTitle) {
        this.creditsTitle = creditsTitle;
    }
    
    
}
